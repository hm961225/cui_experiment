from app.models import db


class CuiExperiment(db.Model):
    __tablename__ = 'cui_experiment'
    id = db.Column(db.Integer, index=True, primary_key=True)
    experiment_name = db.Column(db.String(128), index=True, unique=True)
    classification = db.Column(db.String(128))
    metal_material = db.Column(db.String(128))
    insulation_material = db.Column(db.String(128))
    experimenters = db.Column(db.String(128))
    remarks = db.Column(db.String(128))
    date = db.Column(db.String(128))
    # working condition
    process_description = db.Column(db.String(128))
    experiment_facility_pic = db.Column(db.String(400))
    pipe_structure = db.Column(db.String(128))
    insulation = db.Column(db.String(128))
    piece_number = db.Column(db.String(128))
    piece_geometric_information = db.Column(db.String(128))
    piece_pic = db.Column(db.String(400))
    ring_number = db.Column(db.String(128))
    ring_geometric_information = db.Column(db.String(128))
    ring_pic = db.Column(db.String(400))
    pipe_number = db.Column(db.String(128))
    pipe_geometric_information = db.Column(db.String(128))
    pipe_pic = db.Column(db.String(400))
    sensors_number = db.Column(db.String(128))
    sensors_distribution_pic = db.Column(db.String(400))
    sensors_distribution_table_pic = db.Column(db.String(400))
    sensors_location = db.Column(db.String(400))
    working_temperature_text = db.Column(db.String(128))
    working_temperature_file = db.Column(db.String(400))
    ambient_temperature_file = db.Column(db.String(400))
    ambient_relative_humidity_file = db.Column(db.String(400))
    ambient_temperature_and_humidity_file = db.Column(db.String(400))
    # result
    sensors_temperature_data_file = db.Column(db.String(400))
    sensors_relative_humidity_data_file = db.Column(db.String(400))
    sensors_temperature_humidity_data_file = db.Column(db.String(400))
    localized_pic = db.Column(db.String(400))
    general_pic = db.Column(db.String(400))
    pitting_pic = db.Column(db.String(400))
    stress_pic = db.Column(db.String(400))
    morphology_piece_pic = db.Column(db.String(400))
    morphology_ring_pic = db.Column(db.String(400))
    morphology_pipe_pic = db.Column(db.String(400))
    mass_piece_file = db.Column(db.String(400))
    mass_ring_file = db.Column(db.String(400))
    mass_pipe_file = db.Column(db.String(400))